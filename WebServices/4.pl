opvraagt, is het toegelaten om hier <strong>helemaal niets</strong>
te vermelden. </li>
<li><strong>distinguishedName</strong> : verwijst naar het
AD&nbsp;object in het domein en kan je opzoeken in
<strong>AdsiEdit</strong></li>
</ul>
